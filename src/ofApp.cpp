    frontCam.setPosition(glm::vec3(0, 0, -50));
    frontCam.setNearClip(.1);
    frontCam.setFov(65.5);   // approx equivalent to 28mm in 35mm format
    frontCam.lookAt(glm::vec3(0,0,0));
